-- Este script reflete o mapeamento JPA das entidades, no dialeto H2,
-- equivalente ao que o Hibernate gera com ddl-auto=create-drop.
--
-- Para exportar do banco em execução: acesse o H2 Console em
-- http://localhost:8080/h2-console (JDBC URL: jdbc:h2:mem:agrosat,
-- user: sa, sem senha) e rode:  SCRIPT TO 'schema_export.sql';
